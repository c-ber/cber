//密钥扩展
void KeyExpansion(BYTE key[4][4], BYTE w[4*(Nr+1)][4])
{   
    BYTE temp[4];
    int i,j;
    DWORD wTemp;

    for(i=0;i<Nk;i++)
		for(j=0;j<4;j++)  
			w[i][j]=key[i][j];          
	        
    for(i=Nk;i<4*(Nr+1);i++)
	{	   
       for(j=0;j<4;j++)	   
		   temp[j] = w[i-1][j];               
	   
       if(i%Nk == 0)
       {
          RotWord(temp);                                      
          SubWord(temp);  		  
          wTemp=temp[0]*0x01000000+temp[1]*0x010000+temp[2]*0x0100+temp[3];  //将temp[]转换为DWORD wTemp		 
          wTemp^=Rcon[i/Nk];    		 
          temp[0]=(BYTE)((wTemp&0xFF000000)>>24);
          temp[1]=(BYTE)((wTemp&0x00FF0000)>>16);
          temp[2]=(BYTE)((wTemp&0x0000FF00)>>8);
          temp[3]=(BYTE)(wTemp&0x000000FF);			 
	   }
       else if((Nk==8)&&(i%Nk==4))	  
		   SubWord(temp);          
        
	   for(j=0;j<4;j++)	   
		   w[i][j]=w[i-Nk][j]^temp[j];		   
	} 
}

void RotWord(BYTE temp[4])
{
   int i;
   BYTE j;    
   j=temp[0];
   for(i=0;i<3;i++)
	   temp[i]=temp[i+1];      
   temp[3]=j;   
}

void SubWord(BYTE temp[4])
{
	int i;
	for(i=0;i<4;i++)
		temp[i] = Sbox[temp[i]];
}

void AddRoundKey(BYTE state[4][4],BYTE wTemp[4][4]) //state[i][j]与w[j][i]求和(异或)
{
	int i,j;
	for(i = 0; i < 4; i++)
		for(j = 0; j < 4; j++)		
			state[i][j]^= wTemp[j][i];	
}


void SubBytes(BYTE state[4][4])                       //每个字节的前四位x和后四位y组合成[x,y],查Sbox表
{
	int i,j;
	for(i=0; i<4; i++)
		for(j=0; j<4; j++)			
			state[i][j] = Sbox[state[i][j]];   	
}

void InvSubBytes(BYTE state[4][4])                     //查iSbox表
{
	int i,j;	
	for(i=0; i<4; i++)
		for(j=0; j<4; j++)			
			state[i][j] = iSbox[state[i][j]]; 		
}

void ShiftRows(BYTE state[4][4])              //以字节位单位 第一行不变,第二行循环左移一位,...
{
	int i,j;	
	BYTE temp[4][4];	
		
	for(i=0; i<4; i++)
	{
		temp[0][i]=state[0][i%4];
		temp[1][i]=state[1][(i+1)%4];
		temp[2][i]=state[2][(i+2)%4];
		temp[3][i]=state[3][(i+3)%4];		
	}

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			state[i][j]=temp[i][j];  	
}

void InvShiftRows(BYTE state[4][4])            //以字节位单位 第一行不变,第二行循环右移一位,...
{
	int i,j;	
	BYTE temp[4][4];	
		
	for(i=0; i<4; i++)
	{			
		temp[0][i]=state[0][i%4];            // InvShift(0,4)=0
		temp[1][i]=state[1][(i+3)%4];        // InvShift(1,4)=3
		temp[2][i]=state[2][(i+2)%4];        // InvShift(2,4)=2
		temp[3][i]=state[3][(i+1)%4];		     // InvShift(3,4)=1				
	}

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			state[i][j]=temp[i][j];  		
}

void MixColumns(BYTE state[4][4])                   //伽罗瓦运算{0x53}・{0x02}.... 查表
{
 	int i,j;	
	BYTE temp[4][4];
	for (i=0; i<4; i++)
	{	
		temp[0][i] = GFMul02(state[0][i])^GFMul03(state[1][i])
			^GFMul01(state[2][i])^GFMul01(state[3][i]);
		temp[1][i] = GFMul01(state[0][i])^GFMul02(state[1][i])
			^GFMul03(state[2][i])^GFMul01(state[3][i]);		
		temp[2][i] = GFMul01(state[0][i])^GFMul01(state[1][i])
			^GFMul02(state[2][i])^GFMul03(state[3][i]); 
		temp[3][i] = GFMul03(state[0][i])^GFMul01(state[1][i])
			^GFMul01(state[2][i])^GFMul02(state[3][i]);
	}

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			state[i][j]=temp[i][j];  			
}

void InvMixColumns(BYTE state[4][4])                       //伽罗瓦运算
{
	int i,j;	
	BYTE temp[4][4];
	for (i=0; i<4; i++)
	{	
		temp[0][i] = GFMul0E(state[0][i])^GFMul0B(state[1][i])
			^GFMul0D(state[2][i])^GFMul09(state[3][i]);
		temp[1][i] = GFMul09(state[0][i])^GFMul0E(state[1][i])
			^GFMul0B(state[2][i])^GFMul0D(state[3][i]);		
		temp[2][i] = GFMul0D(state[0][i])^GFMul09(state[1][i])
			^GFMul0E(state[2][i])^GFMul0B(state[3][i]); 
		temp[3][i] = GFMul0B(state[0][i])^GFMul0D(state[1][i])
			^GFMul09(state[2][i])^GFMul0E(state[3][i]);
	}

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			state[i][j]=temp[i][j];
}

void Cipher(BYTE state[4][4],BYTE w[4*(Nr+1)][4])           //加密函数
{	
	int i,j,round;
	BYTE wTemp[4][4];
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			wTemp[i][j]=w[i][j];
		
	AES_DBG("\nCIPHER (ENCRYPT):");		
	AES_DBG("\nround[ 0].input   ");                    //start[0]  = state   state按照列序存储

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			AES_DBG("%02x ",state[j][i]);  
		 			
	AES_DBG("\nround[ 0].k_sch   ");                    //key_sch[0]
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",w[i][j]);

	AddRoundKey(state,wTemp); 
  		
	for(round=1;round<Nr;round++)                       //round 1 --> Nr-1 
	{
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)		
				wTemp[i][j]=w[i+round*4][j];

		AES_DBG("\nround[ %d].start   ",round);          //start[round]
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",state[j][i]);

		SubBytes(state);                                //SubBytes[round]     

		AES_DBG("\nround[ %d].s_box   ",round);         
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)		
				AES_DBG("%02x ",state[j][i]);			

		ShiftRows(state);                               //ShiftRows[round]     

		AES_DBG("\nround[ %d].s_row   ",round);         
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)	
				AES_DBG("%02x ",state[j][i]);
					
		MixColumns(state);                              //MixColumns[round]

		AES_DBG("\nround[ %d].m_col   ",round);
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)		
				AES_DBG("%02x ",state[j][i]);		
	
		AES_DBG("\nround[ %d].k_sch   ",round);          //key_sch[round]
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",w[i+round*4][j]);

		AddRoundKey(state,wTemp);	
	}
	
	for(i=0;i<4;i++)
		for(j=0;j<4;j++){
			wTemp[i][j]=w[i+Nr*4][j];
		}

	AES_DBG("\nround[%d].start   ",Nr);                     //start[Nr]
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);

	SubBytes(state);                                    //SubBytes[Nr]     

	AES_DBG("\nround[%d].s_box   ",Nr);
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);	

	ShiftRows(state);                                   //ShiftRows[Nr]   

	AES_DBG("\nround[%d].s_row   ",Nr);            
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);

	AES_DBG("\nround[%d].k_sch   ",Nr);                     //key_sch[Nr]
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			AES_DBG("%02x ",w[i+Nr*4][j]);

	AddRoundKey(state, wTemp);	
	
	AES_DBG("\nround[%d].output  ",Nr);                      //output --> result
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);
}

//解密函数
void InvCipher(BYTE state[4][4],BYTE w[4*(Nr+1)][4])  
{
	int i,j,round;	
	BYTE wTemp[4][4];
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			wTemp[i][j]=w[i+4*Nr][j];
	
	AES_DBG("\n\nINVERSE CIPHER (DECRYPT):");			
	AES_DBG("\nround[ 0].iinput   ");                    //istart[0] = result   result按照列序存储

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			AES_DBG("%02x ",state[j][i]);  
		 			
	AES_DBG("\nround[ 0].ik_sch   ");                    //key_sch[Nr]      第Nr组扩展密钥
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",w[i+4*Nr][j]);

	AddRoundKey(state, wTemp);
	
	for(round=Nr-1 ;round>0 ;round--)                   //ronud  Nr-1 --> 1 
	{			
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)		
					wTemp[i][j]=w[i+round*4][j];
					
		AES_DBG("\nround[ %d].istart   ",Nr-round);      //istart[Nr-round]
		for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",state[j][i]);
			
		InvShiftRows(state);                            //InvShiftRows[Nr-round]
		
		AES_DBG("\nround[ %d].is_row   ",Nr-round);
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",state[j][i]);
				
		InvSubBytes(state);                             //InvSubBytes[Nr-round]
		
		AES_DBG("\nround[ %d].is_box   ",Nr-round);
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",state[j][i]);
			
		AES_DBG("\nround[ %d].ik_sch   ",Nr-round);       //key_sch[round]
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)
				AES_DBG("%02x ",w[i+4*round][j]);
		
		AddRoundKey(state, wTemp);
		
		AES_DBG("\nround[ %d].ik_add   ",Nr-round);     
	    for(i=0;i<4;i++)
			for(j=0;j<4;j++)		
				AES_DBG("%02x ",state[j][i]);	
		
		InvMixColumns(state);                              //InvMixColumns[Nr-round]
	}

	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			wTemp[i][j]=w[i][j];
		
	InvShiftRows(state);                                   //InvShiftRows[Nr]

	AES_DBG("\nround[%d].is_row   ",Nr);            
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			AES_DBG("%02x ",state[j][i]);

	InvSubBytes(state);                                    //InvSubBytes[Nr] 

	AES_DBG("\nround[%d].is_box   ",Nr);
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);	

    AES_DBG("\nround[%d].ik_sch   ",Nr);                       //key_sch[0]
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)
			AES_DBG("%02x ",w[i][j]);

	AddRoundKey(state, wTemp);	
	
	AES_DBG("\nround[%d].ioutput  ",Nr);                       //ioutput --> result
	for(i=0;i<4;i++)
		for(j=0;j<4;j++)	
			AES_DBG("%02x ",state[j][i]);
}

void strtoHex(char ch[32],BYTE block[16])
{
	int i;	
	for(i=0;i<16;i++)
	{ 
        if((ch[2*i] >= '0' && ch[2*i] <= '9')&&(ch[2*i+1] >= '0' && ch[2*i+1] <= '9'))
            block[i] = ((ch[2*i]-'0') << 4) + ch[2*i+1] - '0';
        else if((ch[2*i] >= 'A' && ch[i] <= 'F')&&(ch[2*i+1] >= 'A' && ch[2*i+1] <= 'F'))
            block[i] = ((ch[2*i]-'A'+10) << 4) + ch[2*i+1] - 'A'+10;
		    else if((ch[2*i] >= 'a' && ch[i] <= 'f')&&(ch[2*i+1] >= 'a' && ch[2*i+1] <= 'f'))
            block[i] = ((ch[2*i]-'a'+10) << 4) + ch[2*i+1] - 'a'+10;
        else                    // error if not hexadecimal
        {
            AES_DBG("It must be in hexadecimal notation\n");
            exit(0);
        }	
	}
}
